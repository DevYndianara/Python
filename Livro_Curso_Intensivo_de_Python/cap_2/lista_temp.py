# A função sorted() possibilita exibir sua lista em uma ordem específica, porém, não afeta a ordem original da lista
cars = ['bmw', 'audi', 'toyota', 'subaru']

#print("Here is the original list")
#print(cars)

print("\nHere is the sorted list")
